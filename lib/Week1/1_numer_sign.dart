/*
Write a Dart program to get a number from the user and print whether it is positive or negative.
*/
void main() {
  int number = 10;

  if (number > 0) {
    print('It is a positive number');
  } else if (number < 0) {
    print('It is a negative number');
  } else {
    print('number = 0');
  }
}
